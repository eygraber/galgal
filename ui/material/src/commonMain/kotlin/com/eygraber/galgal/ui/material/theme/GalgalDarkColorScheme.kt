    onError = GalgalColors.darkOnError,
    errorContainer = GalgalColors.darkErrorContainer,
    onErrorContainer = GalgalColors.darkOnErrorContainer,
    background = GalgalColors.darkBackground,
    onBackground = GalgalColors.darkOnBackground,
    surface = GalgalColors.darkSurface,
    surfaceVariant = GalgalColors.darkSurfaceVariant,
    surfaceContainerLowest = GalgalColors.darkSurfaceContainerLowest,
