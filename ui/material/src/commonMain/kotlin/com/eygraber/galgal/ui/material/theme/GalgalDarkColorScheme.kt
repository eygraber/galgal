    surfaceContainer = GalgalColors.darkSurfaceContainer,
    surfaceContainerHigh = GalgalColors.darkSurfaceContainerHigh,
    surfaceContainerHighest = GalgalColors.darkSurfaceContainerHighest,
    surfaceBright = GalgalColors.darkSurfaceBright,
